function show(myYear){
    var displaySetting = myYear.style.display;
    if(displaySetting == "block"){
        myYear.style.display = "none";
    }
    else{
        
        myYear.style.display = "block";
    }
}


function showYear1(){
    var myYear = document.getElementById("year1");
    show(myYear);
}

